import math

def persegi(sisi):
    hasil = sisi * sisi
    return hasil

def persegiPanjang(p,l):
    hasil = p * l
    return hasil 

def lingkaran (r):
    hasil = math.pi * math.pow(r,2)
    return hasil 

def segitiga(a,t):
    return (a * t) / 2

def jajarGenjang(a,t):
    hasil = a * t
    return hasil


